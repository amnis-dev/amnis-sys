/**
 * Valid key value for a JSON object.
 */
export const regexJsonKey = /^[a-zA-Z_$][a-zA-Z_$0-9]+$/;

/**
 * Unique identifier.
 */
export const regexUuid = /^[A-Za-z0-9_-]{21,36}/;

/**
 * UID identifier.
 */
export const regexReference = /^[A-Za-z0-9_:-]{21,36}/;

/**
 * Alphanumeric
 */
export const regexAlphanumeric = /^[a-zA-Z0-9_]+$/;

/**
 * Web URL
 */
export const regexWebUrl = /https?:\/\/(www\.)?[-a-zA-Z0-9@:%._+~#=]{1,256}\.[a-zA-Z0-9()]{1,6}\b([-a-zA-Z0-9()@:%_+.~#?&//=]*)?/;

/**
 * Regex domain.
 */
export const regexDomain = /^[a-zA-Z0-9][a-zA-Z0-9-]{1,61}[a-zA-Z0-9]\.[a-zA-Z]{2,}$/;

/**
 * Email address
 */
export const regexEmail = /^[\w-.]+@([\w-]+\.)+[\w-]{2,4}$/;

/**
 * JSON Web Tokens
 */
export const regexJwt = /^(?:[\w-]*\.){2}[\w-]*$/;

